// range 800...1181 =381 some inputs are skipped
int TAGGER_TDC_to_n(int tdc_id){
  static int array[400];
  static int initialized=0;

  if(initialized==0){
    initialized=1;
    for(int n=0; n<400; n++) array[n]=-1;
  }
  
  if(tdc_id>1181){
    printf("tagger tdc out of range %i (800...1181)", tdc_id);
    exit(-1);
  }
  // adcs start at 800
  if(array[tdc_id-800]!=-1) return array[tdc_id-800];
   else{ // not found, fill array
   for(int n=0; n<400; n++){
      if(TAGGER_elements[n].TDC==tdc_id){
        array[tdc_id-800]=n;
        return n;
      }
    }
  }
  return -1;
}

